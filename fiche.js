let x = parseInt(prompt("Quel est ton age"));
if (x<=14){
    alert("vous etes un enfant")
}
else if (x<=24){
    alert("vous etes un adolescent")
}
else if (x<=64){
    alert("vous etes un adulte")
}
else if (x>=65){
    alert("vous etes un aine")
}
else{
    alert("vous n'avez pas mis votre age")
}